#include "libft.h"

char*	strchr(const char *s, int c)
{
	char* out = 0;
	if (*s == (char)c)
		out=s;
	while(*s++)
	{
		if (*s == (char)c)
		out=s;
	}
	// if (c == 0 && *s == c)
	// 	out = s;
	return out;
}
